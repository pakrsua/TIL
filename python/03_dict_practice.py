# 딕셔너리 접근

my_dict01 = { 'name' : '유미', 'name' : '웅이'}
my_dict02 = { 'name01' : '유미', 'name02' : '웅이'}

# key값 출력
# 값이 같으면 뒤에 값 출력
print(my_dict01.keys())
print(my_dict02.keys())

# value값 출력
print(my_dict01.values())
print(my_dict02.values())

# 둘 다 출력
print(my_dict01.items())
print(my_dict02.items())